

// JavaScript Break and Continue statement

// JavaScript Break statement

for (let i = 0; i < 10; i++) {
    if (i === 5) {
        break;
    }
    console.log(i);
}

// JavaScript Continue statement

for (let i = 0; i < 10; i++) {
    if (i === 3) {
        continue;
    }
    console.log(i);
}